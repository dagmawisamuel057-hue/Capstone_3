# Net Worth Calculator

# Assets
cash = 5000
investments = 12000
car_value = 8000

# Liabilities
credit_card_debt = 2000
student_loan = 7000

# Calculate totals
total_assets = cash + investments + car_value
total_liabilities = credit_card_debt + student_loan

net_worth = total_assets - total_liabilities

# Display results
print("Total assets: $" + str(total_assets))
print("Total liabilities: $" + str(total_liabilities))
print("Net worth: $" + str(net_worth))