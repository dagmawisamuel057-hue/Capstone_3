# Numeric Conversion Exercises

# Integer conversion
age = "27"
converted_age = int(age)

print("Original value:", age)
print("Converted to integer:", converted_age)

# Float conversion
price = "19.99"
converted_price = float(price)

print("Original value:", price)
print("Converted to float:", converted_price)

# String conversion
number = 100
converted_number = str(number)

print("Original number:", number)
print("Converted to string:", converted_number)

# Type checking
print(type(age))
print(type(converted_age))
print(type(price))
print(type(converted_price))
print(type(number))
print(type(converted_number))